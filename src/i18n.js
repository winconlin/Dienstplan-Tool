export function t(key, ...args) {
    const lang = document.documentElement.lang || 'de';
    let text = i18n[lang]?.[key] || i18n['de']?.[key] || key;
    if (args.length > 0) {
        args.forEach((arg, index) => {
            text = text.replace(`{${index}}`, arg);
        });
    }
    return text;
}
